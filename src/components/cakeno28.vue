            showArray_cqssc:[1],
            history_tables:[1,0,0],
            history_flag:0,
            history_str:"收起",

        };
        return my_data;
    },
    created:function()
    {
        //检测是否登录
        if(this.$store.state.isLogin || (window.sessionStorage.isLogin == "ok"))
        {
           this.global.log('欢迎回来~');
        }
        else
        {
            //没有登录跳转到登录页面
